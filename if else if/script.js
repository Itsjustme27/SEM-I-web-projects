let a = parseInt(prompt("Enter number from 1-7"));

if (a == 1){
    console.log("Sunday");
    document.write("Sunday");
}else if(a == 2){
    console.log("Monday");
    document.write("Monday");
}else if(a == 3){
    console.log("Tuesday");
    document.write("Tuesday");
}else if(a == 4){
    console.log("Wednesday");
    document.write("Wednesday");
}else if(a == 5){
    console.log("Thurday");
    document.write("Thurday");
}else if(a == 6){
    console.log("Friday");
    document.write("Friday");
}else if(a == 7){
    console.log("Saturday");
    document.write("Saturday");
}else{
    console.log("No such day in the week!");
    document.write("No such day in the week!");
}